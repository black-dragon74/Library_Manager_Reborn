    private void bookdateCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_bookdateCaretUpdate
         // Additional Step to enable button
        String a,b;
        a = memname.getText();
        b = bookdate.getText();
        if (a.isEmpty()==false && b.isEmpty() == false){
            depositbtn.setEnabled(true);
        }
    }//GEN-LAST:event_bookdateCaretUpdate

    private void depositbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositbtnActionPerformed
        String id = tfid.getText();
        nick.dbConnectUpdate("delete from book_transactions where Book_Id = '"+id+"';");
        nick.dbConnectUpdate("update book_catalogue set Status = 'AVAILABLE' where Book_Id = '"+id+"';");
        nick.dbConnectUpdate("update book_transaction_log set Date_Deposit = now() where Book_Id = '"+id+"' && Mem_Id = '"+memid.getText()+"';");
        // If no error then success
        if (nick.dbConnectUpdateErr == null){
            nick.showMessage("Book Deposited Successfully");
            rest.doClick();
        }
        else {
            nick.showMessage(nick.dbConnectUpdateErr);
        }
    }//GEN-LAST:event_depositbtnActionPerformed

