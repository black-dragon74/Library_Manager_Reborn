    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = tfid.getText();
        // Initiate Deposition protocol using connectEx5
        nick.connectEx5("select * from book_transactions where Book_Id = '"+id+"';","Mem_Id","Mem_Name","Book_Id","Book_Name","Date_Issued");
        String mid,mname,bname,isdate;
        mid = nick.dbConnectExecute5Out1;
        mname = nick.dbConnectExecute5Out2;
        bname = nick.dbConnectExecute5Out4;
        isdate = nick.dbConnectExecute5Out5;
        if ("something".equals(nick.dbConnectExecute5RsError)){
            memid.setText(mid);
            memname.setText(mname);
            bookname.setText(bname);
            bookdate.setText(isdate);
            depositbtn.setEnabled(true);
        }
        else {
            nick.showMessage("Sorry, No Books Currently Issued With This Id.");
            depositbtn.setEnabled(false);
            tfid.setText(null);
            tfid.requestFocus();
            memid.setText(null);
            memname.setText(null);
            bookname.setText(null);
            bookdate.setText(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

